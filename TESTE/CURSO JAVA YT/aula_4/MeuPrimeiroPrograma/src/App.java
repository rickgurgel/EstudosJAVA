public class App {
    public static void main(String[] args) throws Exception {
        var login = "ola";
        login = "mundo";
        System.out.println(login);

        // variaveis -> tipo : Textos (Strings)
        // variaveis -> tipo : Numeros (Int)
        // variaveis -> tipo : Numeros fracionados (Double)
        // variaveis -> tipo : Booleanos (verdadeiro | falso)

        // TIPOS DE DADOS
        // DECLARAÇÃO:
        // tipo nomeDaReferencia = conteudoDaReferencia; 
        // java moderno usa o 'var' (tipagem dinâmica)
        // versões mais antigas precisa-se tipar
        String logged = "ola";
        int number1 = 10;
        double percent = 24.33;
        boolean isDriver = false;
        System.out.println(logged);
        System.out.println(number1);
        System.out.println(percent);
        System.out.println(isDriver);

        // VARIÁVEIS
        // são espaços de memória alocados (representados por tipos)
    }

}
